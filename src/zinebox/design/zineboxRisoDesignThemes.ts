import type { ZineboxDesignTheme, ZineboxDesignThemeId } from './zineboxDesignThemeTypes';

const RISO_TITLE = "'IBM Plex Sans', 'Inter', sans-serif";
const RISO_BODY = "'IBM Plex Sans', 'Inter', sans-serif";

function risoTheme(
  id: ZineboxDesignThemeId,
  label: string,
  tagline: string,
  paper: string,
  surface: string,
  ink: string,
  muted: string,
  accent: string,
  accentHover: string,
  accentSoft: string,
  layerA: string,
  layerB: string,
  layerC: string,
  appBg: string,
  extra: Partial<ZineboxDesignTheme['cssVars']> = {},
  muiMode: 'light' | 'dark' = 'light',
): ZineboxDesignTheme {
  return {
    id,
    label,
    tagline,
    group: 'riso',
    muiMode,
    muiPrimary: accent,
    muiBackgroundDefault: paper,
    muiBackgroundPaper: surface,
    muiTextPrimary: ink,
    muiTextSecondary: muted,
    cssVars: {
      '--zinebox-paper': paper,
      '--zinebox-surface': surface,
      '--zinebox-ink': ink,
      '--zinebox-muted': muted,
      '--zinebox-accent': accent,
      '--zinebox-accent-hover': accentHover,
      '--zinebox-accent-soft': accentSoft,
      '--zinebox-border': muiMode === 'dark' ? 'rgba(255,255,255,0.12)' : 'rgba(42, 38, 32, 0.14)',
      '--zinebox-border-strong': muiMode === 'dark' ? 'rgba(255,255,255,0.22)' : 'rgba(42, 38, 32, 0.24)',
      '--zinebox-shadow-subtle': `1px 1px 0 color-mix(in srgb, ${layerA} 42%, transparent)`,
      '--zinebox-shadow': `3px 2px 0 ${layerA}`,
      '--zinebox-shadow-hover': `4px 3px 0 ${layerA}, 5px 4px 0 color-mix(in srgb, ${layerB} 22%, transparent)`,
      /* Covers: cyan (layer A) dominant, biased right — pink only as faint second pass */
      '--zinebox-shadow-cover': `8px 3px 0 ${layerA}, 11px 3px 0 color-mix(in srgb, ${layerA} 38%, transparent)`,
      '--zinebox-shadow-cover-hover': `10px 4px 0 ${layerA}, 14px 4px 0 color-mix(in srgb, ${layerA} 32%, transparent)`,
      '--zinebox-shadow-pill': `2px 2px 0 color-mix(in srgb, ${layerA} 72%, transparent)`,
      '--zinebox-shadow-pill-active': `3px 2px 0 ${layerA}`,
      '--zinebox-radius': '2px',
      '--zinebox-title-font': RISO_TITLE,
      '--zinebox-title-weight': '700',
      '--zinebox-title-transform': 'uppercase',
      '--zinebox-body-font': RISO_BODY,
      '--zinebox-app-bg': appBg,
      '--zinebox-chrome-bg': surface,
      '--zinebox-cover-border-width': '2px',
      '--zinebox-riso-layer-a': layerA,
      '--zinebox-riso-layer-b': layerB,
      '--zinebox-riso-layer-c': layerC,
      '--zinebox-riso-shelf-x': '4px',
      '--zinebox-riso-shelf-y': '2px',
      ...extra,
    },
  };
}

