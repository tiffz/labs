import { describe, expect, it } from 'vitest';
import { parseMidiKeyBpm } from './parseMidiKeyBpm';

/** Minimal SMF: one track, set tempo 120 BPM (500000 us/qn), key C major. */
function minimalMidi(): ArrayBuffer {
  const b = [
    0x4d, 0x54, 0x68, 0x64, 0x00, 0x00, 0x00, 0x06, 0x00, 0x00, 0x00, 0x01, 0x01, 0xe0,
    0x4d, 0x54, 0x72, 0x6b, 0x00, 0x00, 0x00, 0x11,
    0x00, 0xff, 0x51, 0x03, 0x07, 0xa1, 0x20, 0x00, 0xff, 0x59, 0x02, 0x00, 0x00, 0x00, 0xff, 0x2f, 0x00,
  ];
  return new Uint8Array(b).buffer;
}

describe('parseMidiKeyBpm', () => {
  it('reads tempo and key from meta', () => {
    const r = parseMidiKeyBpm(minimalMidi());
    expect(r.bpm).toBe(120);
    expect(r.key).toMatch(/C major/);
  });
});
